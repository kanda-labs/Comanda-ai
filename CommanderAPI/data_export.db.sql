BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "bills" (
	"id"	INTEGER,
	"table_id"	INTEGER,
	"table_number"	INTEGER,
	"status"	VARCHAR(32) NOT NULL,
	"created_at"	INTEGER NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "items" (
	"id"	INTEGER,
	"name"	VARCHAR(255) NOT NULL,
	"value"	INT NOT NULL,
	"category"	VARCHAR(32) NOT NULL,
	"description"	VARCHAR(255),
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "order_item_statuses" (
	"id"	INTEGER,
	"order_item_id"	INT NOT NULL,
	"item_id"	INT NOT NULL,
	"order_id"	INT NOT NULL,
	"unit_index"	INT NOT NULL,
	"status"	VARCHAR(32) NOT NULL,
	"updated_at"	BIGINT NOT NULL,
	"updated_by"	VARCHAR(255),
	PRIMARY KEY("id" AUTOINCREMENT),
	CONSTRAINT "fk_order_item_statuses_item_id__id" FOREIGN KEY("item_id") REFERENCES "items"("id") ON DELETE RESTRICT ON UPDATE RESTRICT,
	CONSTRAINT "fk_order_item_statuses_order_id__id" FOREIGN KEY("order_id") REFERENCES "orders"("id") ON DELETE RESTRICT ON UPDATE RESTRICT
);
CREATE TABLE IF NOT EXISTS "order_items" (
	"order_id"	INT NOT NULL,
	"name"	VARCHAR(255) NOT NULL,
	"item_id"	INT NOT NULL,
	"count"	INT NOT NULL,
	"status"	VARCHAR(32) NOT NULL,
	"observation"	VARCHAR(255),
	CONSTRAINT "pk_order_items" PRIMARY KEY("order_id","item_id"),
	CONSTRAINT "fk_order_items_item_id__id" FOREIGN KEY("item_id") REFERENCES "items"("id") ON DELETE RESTRICT ON UPDATE RESTRICT,
	CONSTRAINT "fk_order_items_order_id__id" FOREIGN KEY("order_id") REFERENCES "orders"("id") ON DELETE RESTRICT ON UPDATE RESTRICT
);
CREATE TABLE IF NOT EXISTS "orders" (
	"id"	INTEGER,
	"bill_id"	INTEGER NOT NULL,
	"table_id"	INTEGER NOT NULL,
	"status"	VARCHAR(32) NOT NULL,
	"created_at"	INTEGER NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT),
	FOREIGN KEY("bill_id") REFERENCES "bills"("id"),
	FOREIGN KEY("table_id") REFERENCES "tables"("id")
);
CREATE TABLE IF NOT EXISTS "tables" (
	"id"	INTEGER,
	"bill_id"	INTEGER,
	"number"	INTEGER NOT NULL,
	"created_at"	INTEGER NOT NULL,
	"status"	VARCHAR(32) NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
CREATE TABLE IF NOT EXISTS "users" (
	"id"	INTEGER,
	"name"	VARCHAR(255) NOT NULL,
	"user_name"	VARCHAR(255) NOT NULL,
	"email"	VARCHAR(255),
	"active"	BOOLEAN NOT NULL DEFAULT 1,
	"role"	VARCHAR(20) NOT NULL DEFAULT 'WAITER',
	"created_at"	BIGINT NOT NULL,
	PRIMARY KEY("id" AUTOINCREMENT)
);
